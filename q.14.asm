//MOVE THE BIT4 OF RAM LOCATION 30H TO BIT2 OF A
//INPUT:30H=5FH 0 1 0 1    1 1 1 1
//	   :A=32H   0 0 1 1    0 0 1 0
//OUTPUT:30H=5FH 0 1 0 1    1 1 1 1
//		 :A=36H  0 0 1 1	0 1 1 0

CSEG AT 0H
	MOV 30H,#5FH
	MOV A,#32H
	MOV 20H,30H
	MOV 21H,A
	MOV C,20H.4
	MOV 21H.2,C
	MOV A,21H
	END
	