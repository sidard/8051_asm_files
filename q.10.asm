//CLEAR THE BIT 3 OF THE RAM LOCATION 22H WITHOUT AFFECTING ANY OTHER BIT
//INPUT: ADDRESS IN RAM : 22H
//  22H.7 22H.6 22H.5 22H.4 22H.3 22H.2 22H.1 22H.0
//    1    1      1    1     1      1     1     1
//OUTPUT: ADDRESS IN RAM : 22H
//  22H.7 22H.6 22H.5 22H.4 22H.3 22H.2 22H.1 22H.0
//    1    1      1    1     1      1     1     1

CSEG AT 0H
	MOV 22H,#0X0FF
	CLR 22H.3
	END
	