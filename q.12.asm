//SWAP THE NIBBLES OF R0 AND R1 SO THAT THE LOW NIBBLE OF R0 SWAPS WITH HIGH NIBBLE OF R1 AND
//AND THE HIGH NIBBLE OF R0 SWAPS WITH LOWER NIBBLE OF R1
//INPUT : R0:0X18
//		: R1:0X27
//OUTPUT: R0:0X72
//		: R1:0X81

CSEG AT 0H
	MOV R0,#0X18
	MOV R1,#0X27
	MOV A,R0
	SWAP A //0X81
	MOV R0,A
	MOV A,R1
	SWAP A
	MOV R1,A
	END
	